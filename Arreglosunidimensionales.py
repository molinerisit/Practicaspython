# Definir un arreglo unidimensional
ventas = [20, 35, 50, 15, 25, 60, 40]

# Acceder a un elemento del arreglo (índice 0 basado)
print(ventas[0])  # Muestra 20
print(ventas[3])  # Muestra 15

# Modificar un elemento del arreglo
ventas[3] = 18  # Modificamos el valor en la posición 3
print(ventas[3])  # Muestra 18
